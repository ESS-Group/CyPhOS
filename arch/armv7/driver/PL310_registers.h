#ifndef ARCH_ARMV7_DRIVER_PL310_REGISTERS_H_
#define ARCH_ARMV7_DRIVER_PL310_REGISTERS_H_

/* Base address of L2 Cache controller */
#ifndef PL310_BASE_ADDRESS
#define PL310_BASE_ADDRESS 0x0
#error "No PL310_BASE_ADDRESS"
#endif


/* PL310 L2-Cache controller register offsets */
#define PL310_CACHE_ID 0x0
#define PL310_CACHE_TYPE 0x4
#define PL310_CACHE_CONTROL 0x100
#define PL310_AUXILARY_CONTROL 0x104
#define PL310_TAG_RAM_CONTROL 0x108
#define PL310_DATA_RAM_CONTROL 0x10C
#define PL310_EVENT_COUNTER_CONTROL 0x200
#define PL310_EVENT_COUNTER_COUNT1_CFG 0x204
#define PL310_EVENT_COUNTER_COUNT0_CFG 0x208
#define PL310_EVENT_COUNTER_COUNT1 0x20C
#define PL310_EVENT_COUNTER_COUNT0 0x210

#define PL310_INVALIDATE_BY_WAY 0x77C
#define PL310_CLEAN_BY_WAY 0x7BC
#define PL310_CLEAN_AND_INVALIDATE_BY_WAY 0x7FC
#define PL310_SYNC 0x730
#define PL310_PREFETCH_CONTROL 0xF60
#define PL310_POWER_CONTROL 0xF80
#define PL310_LOCKDOWN_BY_LINE_ENABLE 0x950
#define PL310_LOCKDOWN_BY_LINE_UNLOCK 0x954

/* Lockdown registers */
#define PL310_D_LOCKDOWN__BASE PL310_BASE_ADDRESS + 0x900
#define PL310_D_LOCKDOWN__(N) PL310_D_LOCKDOWN__BASE + (0x8 * N)

#define PL310_I_LOCKDOWN__BASE PL310_BASE_ADDRESS + 0x904
#define PL310_I_LOCKDOWN__(N) PL310_I_LOCKDOWN__BASE + (0x8 * N)

#define PL310_PERF_COUNTER_DISABLED 0x0
#define PL310_PERF_COUNTER_CO 0x1
#define PL310_PERF_COUNTER_DRHIT 0x2
#define PL310_PERF_COUNTER_DRREQ 0x3
#define PL310_PERF_COUNTER_DWHIT 0x4
#define PL310_PERF_COUNTER_DWREQ 0x5
#define PL310_PERF_COUNTER_DWTREQ 0x6
#define PL310_PERF_COUNTER_IRHIT 0x7
#define PL310_PERF_COUNTER_IRREQ 0x8
#define PL310_PERF_COUNTER_WA 0x9
#define PL310_PERF_COUNTER_IPFALLOC 0xA
#define PL310_PERF_COUNTER_EPFHIT 0xB
#define PL310_PERF_COUNTER_EPFALLOC 0xC
#define PL310_PERF_COUNTER_SRRCVD 0xD
#define PL310_PERF_COUNTER_SRCONF 0xE
#define PL310_PERF_COUNTER_EPFRCVD 0xF



#endif /* ARCH_ARMV7_DRIVER_PL310_REGISTERS_H_ */
